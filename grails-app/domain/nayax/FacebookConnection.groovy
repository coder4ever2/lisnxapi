package nayax

import java.util.Date;

class FacebookConnection {
	
		
		
		String userFacebookId
		String url
		String firstName
		String lastName
		String pictureUrl
		String connectionFacebookId
		Date dateCreated
		Date lastUpdated
		boolean pictureIsSilhouette
		boolean appInstalled
		
		static constraints = {
			url(nullable:true)
			firstName(nullable:true)
			lastName(nullable:true)
			pictureUrl(nullable:true)
			connectionFacebookId(nullable:false)
			userFacebookId(nullable:false)
			dateCreated(nullable:true)
			lastUpdated(nullable:true)
		}
		
}
